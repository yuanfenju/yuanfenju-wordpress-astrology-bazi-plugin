abstract class YFJ_Base_Module {

    protected $module_id;  // 例如: 'bazi'
    protected $shortcode;  // 例如: 'yfj_bazi'
    protected $api_endpoint; // API 后缀，例如: '/api/v1/bazi'

    public function __construct() {
        add_shortcode($this->shortcode, [$this, 'render_form']);
        add_action("wp_ajax_yfj_action_{$this->module_id}", [$this, 'handle_ajax']);
        add_action("wp_ajax_nopriv_yfj_action_{$this->module_id}", [$this, 'handle_ajax']);
    }

    // 1. 渲染前台表单
    public function render_form() {
        ob_start();
        $module_id = $this->module_id;
        // 载入特定模块的表单视图
        include YFJ_PLUGIN_DIR . "modules/{$this->module_id}/views/form.php";
        return ob_get_clean();
    }

    // 2. 处理 AJAX 请求 (核心逻辑)
    public function handle_ajax() {
        check_ajax_referer('yfj_nonce', 'nonce');

        $limit_count = (int) get_option('yfj_security_level', '5');
        if ($limit_count <= 0) {
            $limit_count = 10;
        }

        $client_ip = $_SERVER['REMOTE_ADDR'];
        // 如果使用了 CDN 或代理，尝试获取真实 IP
        if (!empty($_SERVER['HTTP_X_FORWARDED_FOR'])) {
            $client_ip = explode(',', $_SERVER['HTTP_X_FORWARDED_FOR'])[0];
        }

        $rate_limit_key = 'yfj_limit_' . md5($client_ip);
        $current_requests = (int) get_transient($rate_limit_key);

        if ($current_requests >= $limit_count) {
            wp_send_json_error('天机不可频繁泄露，请您静心沉淀片刻，1分钟后再结缘测算。');
            return;
        }

        set_transient($rate_limit_key, $current_requests + 1, 60);

        $env = get_option('yfj_environment', 'sandbox');
        $lang = get_option('yfj_language', 'zh-cn');

        // 收集前端提交的所有业务参数
        $post_data = $_POST['form_data'] ?? [];

        if ($env === 'sandbox') {
            // 沙盒模式：获取模拟数据
            $raw_data = $this->get_demo_data($post_data);
            // 【新增】交给数据处理钩子进行二次加工
            $processed_data = $this->process_api_data($raw_data);
            $this->render_result($processed_data, true);
        } else {
            // Live模式：组装 API 请求
            $api_key = get_option('yfj_api_key');
            if(empty($api_key)) wp_send_json_error('生产环境未配置 API Key');

            $payload = array_merge($post_data, ['api_key' => $api_key, 'lang' => $lang]);
            $api_url = "https://api.yuanfenju.com/index.php" . $this->api_endpoint;

            //将具体的 HTTP 请求剥离到独立方法，方便子类进行缓存拦截(缓存)
            $body = $this->fetch_api_data($api_url, $payload);

            // 修改为校验 errcode == 0
            if(isset($body['errcode']) && $body['errcode'] == 0) {
                // 【新增】将 API 真实的原始数据，交给子类的钩子进行二次加工
                $processed_data = $this->process_api_data($body['data']);
                // 用加工后的数据去渲染
                $this->render_result($processed_data, false);
            } else {
                // 修改为读取 errmsg
                wp_send_json_error('接口返回异常: ' . ($body['errmsg'] ?? 'Unknown Error'));
            }
        }
    }

    /**
     * 【生产兼容排错版】：自动 GZIP、跳过 SSL 强校验、提供详细排障
     *  将其设为 protected，以便让子类可以重写此方法来实现 Redis/数据库缓存或注入压缩参数
     */
    protected function fetch_api_data($api_url, $payload) {

        //核心修改点：去掉了强制的 TLS 钩子，直接改为 sslverify => false
        $response = wp_remote_post($api_url, [
            'body'      => $payload,
            'timeout'   => 20,
            'sslverify' => false // 兼容老旧证书链
        ]);

        // 诊断 1：网络层是否报错
        if (is_wp_error($response)) {
            return ['errcode' => -1, 'errmsg' => '网络通信失败: ' . $response->get_error_message()];
        }

        $body = wp_remote_retrieve_body($response);

        // ===== 【智能压缩处理逻辑开始】 =====
        if ( strlen( $body ) >= 2 && bin2hex( substr( $body, 0, 2 ) ) === '1f8b' ) {
            if ( function_exists( 'gzdecode' ) ) {
                $decoded_body = @gzdecode( $body );
                if ( $decoded_body !== false ) {
                    $body = $decoded_body;
                } else {
                    return ['errcode' => -1, 'errmsg' => '核心组件异常：GZIP 字节流解压失败！'];
                }
            } else {
                return ['errcode' => -1, 'errmsg' => '环境缺陷：用户服务器 PHP 未开启 zlib (gzdecode) 扩展！'];
            }
        }
        // ===== 【智能压缩处理逻辑结束】 =====

        $data = json_decode($body, true);

        // 诊断 2：数据解析是否成功
        if ($data === null && json_last_error() !== JSON_ERROR_NONE) {
            $preview = mb_substr(strip_tags($body), 0, 50, 'UTF-8');
            return [
                'errcode' => -1,
                'errmsg' => 'API 数据解析异常: ' . json_last_error_msg() . ' | 原始报文截取: ' . $preview
            ];
        }

        return $data;
    }

    // 3. 渲染结果视图并返回 JSON
    protected function render_result($data, $is_demo) {
        ob_start();
        // 载入特定模块的结果视图，传入 $data 变量供视图使用
        include YFJ_PLUGIN_DIR . "modules/{$this->module_id}/views/result.php";
        $html = ob_get_clean();

        wp_send_json_success([
            'html' => $html,
            'is_demo' => $is_demo
        ]);
    }

    // 4. 强制子类必须实现沙盒数据方法
    abstract protected function get_demo_data($post_data);

    /**
     * 数据处理钩子方法 (模板方法模式)
     * 基类默认原样返回。如果某个测算模块需要对 API 数据进行二次计算或格式转换，请在子类中重写此方法。
     */
    protected function process_api_data($raw_data) {
        return $raw_data;
    }

