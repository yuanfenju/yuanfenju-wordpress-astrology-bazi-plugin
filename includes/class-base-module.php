    /**
     * 🛡️ 终极智能防盗刷引擎：防伪造 IP + 独立保险丝 + 防止共享IP误伤 + 标准 429
     * @param string $action_type 动作标识 (如 'main', 'liurishi', 'zwlpan')
     * @param int $multiplier 乘数 (短时宽容度)
     */
    protected function check_rate_limit($action_type = 'main', $multiplier = 1) {
        // 1. 【防御性编程】动作白名单
        $allowed_actions = ['main', 'liurishi', 'zwlpan'];
        if (!in_array($action_type, $allowed_actions, true)) {
            $action_type = 'main';
        }

        // 2. 【防伪造与兼容】优先真实 IP
        $client_ip = $_SERVER['REMOTE_ADDR'] ?? '';
        if (!empty($_SERVER['HTTP_CF_CONNECTING_IP'])) {
            $client_ip = $_SERVER['HTTP_CF_CONNECTING_IP'];
        }
        $client_ip = trim($client_ip);

        // 3. 🚀【全新优化】生成更精准的用户标识 (降低共享 IP 误伤)
        $user_id = get_current_user_id();
        if ($user_id) {
            $identifier = 'u_' . $user_id; // 已登录用户直接认 ID
        } else {
            $user_agent = $_SERVER['HTTP_USER_AGENT'] ?? '';
            // 组合 IP 和 浏览器UA 生成 Hash，同 IP 下不同设备会被区分开
            $identifier = 'ip_' . md5($client_ip . $user_agent);
        }

        // 4. 读取站长基础安全等级
        $base_limit = (int) get_option('yfj_security_level', '5');
        if ($base_limit <= 0) { $base_limit = 10; }

        // 5. 【核心解绑】计算双重阈值
        $minute_limit = $base_limit * $multiplier;
        $fuse_limit   = max(100, $base_limit * 20); // 10分钟保险丝独立计算

        // 6. 🚀【全新优化】使用更短的规范化 Key 名 (rl = rate limit)
        $minute_key = 'yfj_rl_m_' . $action_type . '_' . $identifier;
        $fuse_key   = 'yfj_rl_f_' . $identifier;

        $current_minute = (int) get_transient($minute_key);
        $current_fuse   = (int) get_transient($fuse_key);

        // 7. 🚀【智能兼容】主表单返回 200 让前端老 JS 正常读取提示，微交互保持标准的 429
        $status_code = ($action_type === 'main') ? 200 : 429;

        if ($current_minute >= $minute_limit) {
            wp_send_json_error('操作过于频繁，请稍息片刻再试。', $status_code);
        }
        if ($current_fuse >= $fuse_limit) {
            wp_send_json_error('您的测算频率已触发系统底层保护机制，请 10 分钟后再试。', $status_code);
        }

        // 8. 记录请求次数
        set_transient($minute_key, $current_minute + 1, 60);
        if ($current_fuse === 0) {
            set_transient($fuse_key, 1, 600); // 10分钟保险丝
        } else {
            set_transient($fuse_key, $current_fuse + 1, 600);
        }
    }

    // 2. 处理 AJAX 请求 (核心逻辑)
    public function handle_ajax() {
        check_ajax_referer('yfj_nonce', 'nonce');

        // 主表单，标准频率，乘数为 1
        $this->check_rate_limit('main', 1);

        $env = get_option('yfj_environment', 'sandbox');
        $lang = get_option('yfj_language', 'zh-cn');

        // 收集前端提交的所有业务参数
        $post_data = $_POST['form_data'] ?? [];

        if ($env === 'sandbox') {
            // 沙盒模式：获取模拟数据
            $raw_data = $this->get_demo_data($post_data);
            // 【新增】交给数据处理钩子进行二次加工
            $processed_data = $this->process_api_data($raw_data);
            $this->render_result($processed_data, true, $post_data);
        } else {
            // Live模式：组装 API 请求
            $api_key = get_option('yfj_api_key');
            if(empty($api_key)) wp_send_json_error('生产环境未配置 API Key');

            $payload = array_merge($post_data, ['api_key' => $api_key, 'lang' => $lang]);
            $api_url = "https://api.yuanfenju.com/index.php" . $this->api_endpoint;

            //将具体的 HTTP 请求剥离到独立方法，方便子类进行缓存拦截(缓存)
            $body = $this->fetch_api_data($api_url, $payload);

            // 修改为校验 errcode == 0
            if(isset($body['errcode']) && $body['errcode'] == 0) {
                // 【新增】将 API 真实的原始数据，交给子类的钩子进行二次加工
                $processed_data = $this->process_api_data($body['data']);
                // 用加工后的数据去渲染
                $this->render_result($processed_data, false, $post_data);
            } else {
                // 修改为读取 errmsg
                wp_send_json_error('接口返回异常: ' . ($body['errmsg'] ?? 'Unknown Error'));
            }
        }
    }

    /**
     * 【生产兼容排错版】：自动 GZIP、跳过 SSL 强校验、提供详细排障
     *  将其设为 protected，以便让子类可以重写此方法来实现 Redis/数据库缓存或注入压缩参数
     */
    protected function fetch_api_data($api_url, $payload) {

        //核心修改点：去掉了强制的 TLS 钩子，直接改为 sslverify => false
        $response = wp_remote_post($api_url, [
            'body'      => $payload,
            'timeout'   => 20,
            'sslverify' => false // 兼容老旧证书链
        ]);

        // 诊断 1：网络层是否报错
        if (is_wp_error($response)) {
            return ['errcode' => -1, 'errmsg' => '网络通信失败: ' . $response->get_error_message()];
        }

        $body = wp_remote_retrieve_body($response);

        // ===== 【智能压缩处理逻辑开始】 =====
        if ( strlen( $body ) >= 2 && bin2hex( substr( $body, 0, 2 ) ) === '1f8b' ) {
            if ( function_exists( 'gzdecode' ) ) {
                $decoded_body = @gzdecode( $body );
                if ( $decoded_body !== false ) {
                    $body = $decoded_body;
                } else {
                    return ['errcode' => -1, 'errmsg' => '核心组件异常：GZIP 字节流解压失败！'];
                }
            } else {
                return ['errcode' => -1, 'errmsg' => '环境缺陷：用户服务器 PHP 未开启 zlib (gzdecode) 扩展！'];
            }
        }
        // ===== 【智能压缩处理逻辑结束】 =====

        $data = json_decode($body, true);

        // 诊断 2：数据解析是否成功
        if ($data === null && json_last_error() !== JSON_ERROR_NONE) {
            $preview = mb_substr(strip_tags($body), 0, 50, 'UTF-8');
            return [
                'errcode' => -1,
                'errmsg' => 'API 数据解析异常: ' . json_last_error_msg() . ' | 原始报文截取: ' . $preview
            ];
        }

        return $data;
    }

    // 3. 渲染结果视图并返回 JSON
    protected function render_result($data, $is_demo, $params = []) {
        ob_start();
        // 载入特定模块的结果视图，传入 $data 变量供视图使用
        include YFJ_PLUGIN_DIR . "modules/{$this->module_id}/views/result.php";
        $html = ob_get_clean();

        wp_send_json_success([
            'html' => $html,
            'is_demo' => $is_demo
        ]);
    }

